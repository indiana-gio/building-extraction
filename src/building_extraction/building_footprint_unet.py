"""## 8. Inference on the 10 tiles — sliding window with overlap blending

Each 5000×5000 tile is processed in 512×512 windows with 50% overlap. Overlapping probabilities are averaged with a cosine-ramp weight (higher confidence at window centers, lower at edges), which removes visible seams. Outputs per tile:
- `*_prob.tif` — float32 probability raster (georeferenced)
- `*_pred.tif` — uint8 binary mask
"""

def make_blend_weight(patch):
    ramp = np.hanning(patch)
    w = np.outer(ramp, ramp).astype(np.float32)
    w = np.clip(w, 1e-3, None)
    return w

@torch.no_grad()
def predict_tile(tile_path, model, out_dir, patch, stride, batch_size=8):
    with rasterio.open(tile_path) as src:
        H, W = src.height, src.width
        profile = src.profile.copy()
        img = src.read().astype(np.float32)  # (4, H, W) — ~400 MB per tile, fits in RAM

    # Ensure img remains float32 after normalization, as BAND_MEANS and BAND_STDS are float64
    img = ((img - BAND_MEANS[:, None, None]) / BAND_STDS[:, None, None]).astype(np.float32)

    prob_acc   = np.zeros((H, W), dtype=np.float32)
    weight_acc = np.zeros((H, W), dtype=np.float32)
    wpatch = make_blend_weight(patch)

    ys = list(range(0, H - patch + 1, stride))
    xs = list(range(0, W - patch + 1, stride))
    if ys[-1] != H - patch: ys.append(H - patch)
    if xs[-1] != W - patch: xs.append(W - patch)
    positions = [(y, x) for y in ys for x in xs]

    for b in tqdm(range(0, len(positions), batch_size),
                  desc=Path(tile_path).stem, leave=False):
        batch_pos = positions[b:b + batch_size]
        batch = np.stack([img[:, y:y+patch, x:x+patch] for (y, x) in batch_pos])
        t = torch.from_numpy(batch).to(DEVICE)
        with torch.autocast(device_type="cuda", enabled=CONFIG["amp"] and DEVICE == "cuda"):
            p = torch.sigmoid(model(t))[:, 0].float().cpu().numpy()
        for (y, x), pi in zip(batch_pos, p):
            prob_acc[y:y+patch, x:x+patch]   += pi * wpatch
            weight_acc[y:y+patch, x:x+patch] += wpatch

    prob = prob_acc / weight_acc
    pred = (prob >= CONFIG["prob_threshold"]).astype("uint8")

    stem = Path(tile_path).stem
    p_prof = profile.copy(); p_prof.update(count=1, dtype="float32", nodata=None, compress="lzw")
    with rasterio.open(out_dir / f"{stem}_prob.tif", "w", **p_prof) as dst:
        dst.write(prob, 1)
    m_prof = profile.copy(); m_prof.update(count=1, dtype="uint8", nodata=None, compress="lzw")
    with rasterio.open(out_dir / f"{stem}_pred.tif", "w", **m_prof) as dst:
        dst.write(pred, 1)
    return prob, pred

pred_dir = WORK / "predictions"
for tp in infer_tile_paths:
    predict_tile(tp, model, pred_dir, CONFIG["infer_patch"], CONFIG["infer_stride"])
print("Done. Rasters written to", pred_dir)

"""## 9. Vectorize predictions to building polygons

Converts each binary raster to polygons, drops slivers below `min_building_px`, applies a light simplify, and writes one GeoPackage with all 10 tiles combined — ready to load in ArcGIS Pro.
"""

all_polys = []
for tp in infer_tile_paths:
    stem = Path(tp).stem
    with rasterio.open(pred_dir / f"{stem}_pred.tif") as src:
        pred = src.read(1)
        transform, crs = src.transform, src.crs
        px_area = abs(transform.a * transform.e)

    for geom, val in rio_features.shapes(pred, mask=pred == 1, transform=transform):
        poly = shape(geom)
        if poly.area / px_area < CONFIG["min_building_px"]:
            continue
        all_polys.append({"geometry": poly, "tile": stem,
                          "area_m2": round(poly.area, 1)})

gdf_pred = gpd.GeoDataFrame(all_polys, crs=crs)
gdf_pred["geometry"] = gdf_pred.geometry.simplify(abs(transform.a) * 0.75)  # ~sub-pixel simplify
out_gpkg = pred_dir / "predicted_footprints.gpkg"
gdf_pred.to_file(out_gpkg, driver="GPKG")
print(f"{len(gdf_pred)} predicted footprints → {out_gpkg}")

"""## Notes & next steps

- **Regularization**: raw CNN polygons have wobbly edges. For cleaner cartographic footprints, consider orthogonalization (e.g., ArcGIS Pro *Regularize Building Footprint* tool, or the `buildingregulariser` Python package).
- **Encoder size**: `resnet34` is a strong baseline; `resnet50` or `efficientnet-b3` may add 1–3 IoU points at higher memory cost.
- **Class imbalance**: if buildings are sparse, try `smp.losses.TverskyLoss(alpha=0.3, beta=0.7)` to penalize missed buildings more.
- **Test-time augmentation**: averaging predictions over flips/rotations (e.g., with the `ttach` package) typically adds ~0.5–1 IoU.
- **Benchmark comparison**: the `*_prob.tif` outputs let you compute PR curves at multiple thresholds, useful when comparing against Blackshark.ai / Living Atlas outputs on the same tiles.
"""