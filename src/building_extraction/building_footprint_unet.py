"""## 5. Model — U-Net with ResNet-34 encoder

`segmentation_models_pytorch` adapts the ImageNet-pretrained first conv to 4 input channels (the pretrained RGB weights are reused and the extra channel is initialized sensibly), which typically converges much faster than training from scratch.
"""

model = smp.Unet(
    encoder_name=CONFIG["encoder"],
    encoder_weights=CONFIG["encoder_weights"],
    in_channels=CONFIG["in_channels"],
    classes=1,
).to(DEVICE)

dice_loss = smp.losses.DiceLoss(mode="binary")
bce_loss  = nn.BCEWithLogitsLoss()

def criterion(logits, target):
    return 0.5 * bce_loss(logits, target) + 0.5 * dice_loss(logits, target)

optimizer = torch.optim.AdamW(model.parameters(), lr=CONFIG["lr"],
                              weight_decay=CONFIG["weight_decay"])
scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=CONFIG["epochs"])
scaler = torch.cuda.amp.GradScaler(enabled=CONFIG["amp"] and DEVICE == "cuda")

@torch.no_grad()
def evaluate(model, loader):
    model.eval()
    tp = fp = fn = tn = 0
    loss_sum, n = 0.0, 0
    for img, msk in loader:
        img, msk = img.to(DEVICE), msk.to(DEVICE)
        with torch.autocast(device_type="cuda", enabled=CONFIG["amp"] and DEVICE == "cuda"):
            logits = model(img)
            loss = criterion(logits, msk)
        loss_sum += loss.item() * img.size(0); n += img.size(0)
        pred = (torch.sigmoid(logits) > 0.5).long()
        t = msk.long()
        _tp, _fp, _fn, _tn = smp.metrics.get_stats(pred, t, mode="binary")
        tp += _tp.sum(); fp += _fp.sum(); fn += _fn.sum(); tn += _tn.sum()
    iou = (tp / (tp + fp + fn + 1e-9)).item()
    f1  = (2 * tp / (2 * tp + fp + fn + 1e-9)).item()
    return loss_sum / max(n, 1), iou, f1

