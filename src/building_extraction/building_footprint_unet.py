class BuildingDataset(Dataset):
    def __init__(self, records, split, augment=False):
        self.recs = [r for r in records if r["split"] == split]
        self.augment = augment

    def __len__(self):
        return len(self.recs)

    def __getitem__(self, i):
        r = self.recs[i]
        img = np.load(WORK / "chips/images" / r["img"]).astype(np.float32)  # (4,H,W)
        msk = np.load(WORK / "chips/masks"  / r["msk"]).astype(np.float32)  # (H,W)

        if self.augment:
            # albumentations expects HWC
            out = train_aug(image=np.moveaxis(img, 0, -1), mask=msk)
            img = np.moveaxis(out["image"], -1, 0)
            msk = out["mask"]

        img = (img - BAND_MEANS[:, None, None]) / BAND_STDS[:, None, None]
        return torch.from_numpy(img.astype(np.float32)), torch.from_numpy(msk)[None, ...]

